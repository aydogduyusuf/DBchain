package api

import (
	"database/sql"
	"errors"
	"fmt"
	"net/http"

	db "github.com/aydogduyusuf/DBchain/db/sqlc"
	"github.com/aydogduyusuf/DBchain/access_refresh_tokens"
	"github.com/gin-gonic/gin"
)

type transactionRequest struct {
	TransactionType  string	`json:"transaction_type"`
	FromAddress      int64  `json:"from_address" binding:"required,min=24" `
	ToAddress        int64  `json:"to_address"`
	TransferData	 string `json:"transfer_data"`
	HashValue 		 string `json:"hash_value"`
}

func (server *Server) createTransaction(ctx *gin.Context) {
	var req transactionRequest
	if err := ctx.ShouldBindJSON(&req); err != nil {
		ctx.JSON(http.StatusBadRequest, errorResponse(err))
		return
	}

	fromAccount, valid := server.validAccount(ctx, req.FromAccountID, req.Currency)
	if !valid {
		return
	}

	authPayload := ctx.MustGet(authorizationPayloadKey).(*token.Payload)
	if fromAccount.Owner != authPayload.Username {
		err := errors.New("from account does not belong to the authenticated user")
		ctx.JSON(http.StatusUnauthorized, errorResponse(err))
		return
	}

	_, valid = server.validAccount(ctx, req.ToAccountID, req.Currency)
	if !valid {
		return
	}

	arg := db.TransferTxParams{
		FromAccountID: req.FromAccountID,
		ToAccountID: req.ToAccountID,
		Amount: req.Amount,
	}

	result, err := server.store.TransferTx(ctx, arg)
	if err != nil {
		ctx.JSON(http.StatusInternalServerError, errorResponse(err))
		return
	}

	ctx.JSON(http.StatusOK, result)
}

